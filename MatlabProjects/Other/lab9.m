% Генерування синтетичного часового ряду для ціни срібла до долара США
rng(42); % Встановлення seed для відтворюваності результатів
n = 200; % Кількість днів
mean_price = 25; % Середня ціна
std_dev = 2; % Стандартне відхилення

% Генерування синтетичних даних для ціни срібла (нормальний розподіл)
prices = mean_price + std_dev * randn(n, 1);

% Побудова графіка цінового ряду
plot(prices);
title('Синтетичний часовий ряд ціни срібла до долара США');
xlabel('День');
ylabel('Ціна, USD');

% Перевірка стаціонарності часового ряду
half = floor(length(prices) / 2); % Розділення часового ряду на дві половини
first_half = prices(1:half); % Перша половина часового ряду
second_half = prices(half+1:end); % Друга половина часового ряду

% Перевірка стаціонарності за допомогою тесту на рівність середніх значень
[h, p] = ttest(first_half, second_half);
if h == 0
    disp('Часовий ряд є нестаціонарним.');
else
    disp('Часовий ряд є стаціонарним.');
end
disp(['p-значення: ', num2str(p)]);
