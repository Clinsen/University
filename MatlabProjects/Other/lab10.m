% Випадкове генерування фінансових даних
rng(42); % Задаємо seed для відтворюваності результатів
n = 200; % Кількість днів
mean_price = 25; % Середня ціна
std_dev = 2; % Стандартне відхилення

% Генерування синтетичного часового ряду ціни срібла
prices = mean_price + std_dev * randn(n, 1);

% Параметр ковзного вікна
window_size = 10;

% Розрахунок кривої середнього ковзного
moving_avg = movmean(prices, window_size);

% Побудова графіка
plot(prices, 'b', 'LineWidth', 1.5); % Оригінальний часовий ряд
hold on;
plot(moving_avg, 'r', 'LineWidth', 2); % Крива середнього ковзного
title('Ціна срібла до долара США та її крива середнього ковзного');
xlabel('Час');
ylabel('Ціна, USD');
legend('Оригінальний ряд', 'Середнє ковзне');
grid on;
hold off;
